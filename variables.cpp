#include<iostream>
int main (){
    //this is for (whole number) 
    int age = 18;
    std::cout<<"age= "<< age <<std::endl;
    //this is for (decimal number)
    double percantage=83.33;
    std::cout<< "percantage= " << percantage <<std::endl;
    //this is for single character
    char grade= 'A';
    std::cout<<"grade= "<<grade<<std::endl;
    //this is for single line statement
    std::string name ="Sudarshan Agrawal";
    std::cout<<"name= "<<name<<std::endl;
    //this is for (true or false)
    bool gender = true;
    std::cout<<"male= "<<gender<<std::endl;
    return 0;
}